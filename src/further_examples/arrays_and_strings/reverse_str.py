reverse_string = lambda s: s[::-1]


def main():
    s1 = 'abcdefg'
    s2 = 'buffy'
    s3 = ''
    print(reverse_string(s1))
    print(reverse_string(s2))
    print(reverse_string(s3))
                   
if __name__ == '__main__':
    main()

